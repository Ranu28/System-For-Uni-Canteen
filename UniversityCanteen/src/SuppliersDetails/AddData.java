    PreparedStatement statement;
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        supllierFirstName = txtFirstName.getText();
        supllierLastName = txtLastName.getText();
        supllierAddress = txtAddress.getText();
       supllierContactNo = txtContactNo.getText();
        supllierEmail = txtEmail.getText();
        companyName = txtCompanyName.getText();
        companyEmail =txtCompanyEmail.getText();
        companyAddress = txtCompanyAddress.getText();
        companyContactNo= txtCompanyContact.getText();
        
        try{
        
            String insertQry = "INSERT INTO `supplier_details`( `SupplierFirstName`,"
                    + " `SupplierLastName`, `SupplierContactNo`, `SupplierAddress`, `SupplierEmail`, "
                    + "`CompanyName`, `CompanyContactNo`, `CompanyAddress`, `CompanyEmail`) VALUES"
                    + " (?,?,?,?,?,?,?,?,?)";
            
            statement = DBconnect.getConnect().prepareStatement(insertQry);
            
            statement.setString(1,supllierFirstName);
            statement.setString(2,supllierLastName);
            statement.setInt(3,Integer.parseInt(supllierContactNo));
            statement.setString(4,supllierAddress);
            statement.setString(5,supllierEmail);
            statement.setString(6,companyName);
            statement.setInt(7,Integer.parseInt(companyContactNo));
            statement.setString(8,companyAddress);
            statement.setString(9,companyEmail);
           
            statement.executeUpdate();
            JOptionPane.showMessageDialog(null, "Details Updated succesfully");
            
        }catch(SQLException ex){
            
            JOptionPane.showMessageDialog(null, "Error "+ex);
            System.out.println("Error "+ex);
        
        }
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

